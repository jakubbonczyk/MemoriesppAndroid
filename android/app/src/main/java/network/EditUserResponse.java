package network;

public class EditUserResponse {
    private Integer id;
    private String login;
    private String name;
    private String surname;
    private String role;

    public Integer getId() {
        return id;
    }

    public String getLogin() {
        return login;
    }

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public String getRole() {
        return role;
    }
}
